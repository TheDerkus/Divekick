import genalgo
import random

def test_genalgo():
    expected = [
        [0.4813121368149009, 0.4813121368149009, 0.0373757263701982],
        [0.5, 0.5, 0.0],
        [0.1, 0.2, 0.7],
        [0.1368203180312621, 0.18450401997248886, 0.678675661996249],
        [0.6000000000000001, 0.30000000000000004, 0.10000000000000002],
        [0.6077991132363388, 0.3038995566181694, 0.08830133014549184],
        [0.15, 0.14, 0.71],
        [0.1453806503027077, 0.13568860694919385, 0.7189307427480984],
    ]
    POPULATION = [[.5, .5, 0], [.1, .2, .7], [.6, .3, .1], [.15, .14, .71],]
    SCORES = [9, 4, 5, 8,]
    z = zip(SCORES, POPULATION)
    random.seed(6942069)
    calculated = genalgo.Generation(z).next()
    print(calculated)
    print(expected)
    assert calculated == expected

test_genalgo()
